
红黑树必须满足以下5个条件

1> 根节点必须是黑色
2> 节点的颜色必须是黑色 或者 红色
3> 叶子节点可以是黑色或者红色, 假想节点(null 节点) 也必须是黑色  ?
4> red 节点的子节/父节点点都是黑色
5> 从任一节点到叶子节点的所有路劲都包含相同数目的 黑节点
   且 从任一节点到假想节点(null)的所有路径包含相同数目的黑节点




红黑树与B树的类推
1>红黑树和 4 阶B树 (2-3-4树) 具有等价性
2>黑色节点与他的红色子节点融合在一起, 形成一个B数的节点, 如果没有红色子节点就独立称为一个节点
3> 从上面2第二条融合的概念可以推断出, 红色树中, 黑色节点(非null节点)的个数就是 4阶B数的总节点树
4> 红色树只能和4阶 B树进行类比, 如果是其它阶的B树相比较都是不严谨的




